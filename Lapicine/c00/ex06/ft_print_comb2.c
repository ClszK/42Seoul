#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	arr1[5];

	i = 0;
	while (i <= 98)
	{
		arr1[0] = i / 10 + '0';
		arr1[1] = i % 10 + '0';
		arr1[2] = ' ';
		j = i++;
		while (++j <= 99)
		{
			arr1[3] = j / 10 + '0';
			arr1[4] = j % 10 + '0';
			write(1, arr1, 5);
			if (i != 99)
				write(1, ", ", 2);
		}	
	}
}
