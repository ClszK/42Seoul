void	ft_swap(int *a, int *b)
{
	int	tmp;

	tmp = *(a);
	*(a) = *(b);
	*(b) = tmp;
}

void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;

	i = 0;
	j = size - 1;
	while (j > i)
		ft_swap(tab + i++, tab + j--);
}
