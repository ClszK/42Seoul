char	*ft_strupcase(char *str)
{
	char	*r_str;

	r_str = str;
	while (*(str))
	{
		if (*(str) >= 'a' && *(str) <= 'z' )
				*(str) -= 32;
		str++;
	}
	return (r_str);
}
