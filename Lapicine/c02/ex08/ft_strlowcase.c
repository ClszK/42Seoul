char	*ft_strlowcase(char *str)
{
	char	*r_str;

	r_str = str;
	while (*(str))
	{
		if (*(str) >= 'A' && *(str) <= 'Z' )
				*(str) += 32;
		str++;
	}
	return (r_str);
}
