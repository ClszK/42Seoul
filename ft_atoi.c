int	ft_atoi(const char *str)
{
	int	sign;
	int	r_num;

	sign = 1;
	r_num = 0;
	while ((*(str) >= 8 && *(str) <= 13) || *(str) == 32)
		str++;
	if (*(str) == '-' || *(str) == '+')
		if (*(str++) == '-')
			sign *= -1;
	while (*(str) && *(str) >= '0' && *(str) <= '9')
	{
		r_num = r_num * 10 + (*(str) - '0');
		str++;
	}
	return (r_num * sign);
}
